def _path_time_sort_key(path: Path) -> tuple[int, str]:
	digits = "".join(re.findall(r"\d", path.stem))
	return (int(digits[:12]) if len(digits) >= 12 else 0, path.name)


def assign_truth_tracks_to_signal_tracks(truth_tracks: list[Any], signal_tracks: list[GpsTrack]) -> dict[str, Any]:
	kml_truths = [track for track in truth_tracks if track.path.suffix.lower() == ".kml"]
	csv_truths = [track for track in truth_tracks if track.path.suffix.lower() != ".kml"]
	if not kml_truths or not csv_truths or len(truth_tracks) != len(signal_tracks) or len(kml_truths) >= len(signal_tracks):
		return assign_reconstructions_to_gps(truth_tracks, signal_tracks)

	ordered_signals = sorted(signal_tracks, key=lambda track: _path_time_sort_key(track.path))
	kml_signal_paths = {track.path.name for track in ordered_signals[-len(kml_truths):]}
	kml_signals = [track for track in signal_tracks if track.path.name in kml_signal_paths]
	csv_signals = [track for track in signal_tracks if track.path.name not in kml_signal_paths]
	assignments: dict[str, Any] = {}
	if csv_signals:
		assignments.update(assign_reconstructions_to_gps(csv_truths, csv_signals))
	for signal_track, truth_track in zip(
		sorted(kml_signals, key=lambda track: _path_time_sort_key(track.path)),
		sorted(kml_truths, key=lambda track: _path_time_sort_key(track.path)),
	):
		assignments[signal_track.path.name] = truth_track
	return assignments


def read_signal_segment_wgs(path: Path) -> GpsTrack:
	records: list[tuple[int | None, str, Point]] = []
	declared_start_ms, declared_end_ms = parse_gps_filename_window(path)
	with path.open(encoding="gbk", errors="ignore", newline="") as handle:
		for row in csv.reader(handle):
			cells = [str(cell).strip().strip("\t").strip() for cell in row]
			numbers = [parse_float(cell) for cell in cells]
			nums = [num for num in numbers if num is not None]
			if len(nums) >= 3 and nums[1] > 100 and 30 <= nums[2] <= 50:
				lon, lat = nums[1], nums[2]
				source_time = cells[0] if cells else ""
			elif len(nums) >= 2 and nums[0] > 100 and 30 <= nums[1] <= 50:
				lon, lat = nums[0], nums[1]
				source_time = cells[0] if cells else ""
			else:
				continue
			time_ms = parse_datetime_ms(source_time)
			if declared_start_ms is not None and declared_end_ms is not None:
				if time_ms is None or time_ms < declared_start_ms or time_ms > declared_end_ms:
					continue
			records.append((time_ms, source_time, Point(lat=lat, lon=lon)))
	records.sort(key=lambda item: (item[0] is None, item[0] or 0, item[1]))
	points: list[Point] = []
	source_times: list[str] = []
	source_time_ms: list[int | None] = []
	for time_ms, source_time, point in records:
		if points and abs(points[-1].lat - point.lat) < 1e-10 and abs(points[-1].lon - point.lon) < 1e-10:
			continue
		points.append(point)
		source_times.append(source_time)
		source_time_ms.append(time_ms)
	if len(points) < 2:
		raise ValueError(f"signal segment csv has fewer than 2 usable points: {path}")
	valid_times = [time_ms for time_ms in source_time_ms if time_ms is not None]
	return GpsTrack(
		path=path,
		points=points,
		source_times=source_times,
		source_time_ms=source_time_ms,
		start_ms=min(valid_times) if valid_times else None,
		end_ms=max(valid_times) if valid_times else None,
		declared_start_ms=declared_start_ms,
		declared_end_ms=declared_end_ms,
	)


