def _build_interpolated_row_times(rows: list[dict[str, Any]]) -> dict[int, float | None]:
	groups: dict[str, list[tuple[int, dict[str, Any]]]] = {}
	for index, row in enumerate(rows):
		group_key = str(row.get("od_segment_idx") or row.get("segment_idx") or "0")
		groups.setdefault(group_key, []).append((index, row))
	result: dict[int, float | None] = {}
	for group_rows in groups.values():
		group_rows.sort(key=lambda item: parse_float(item[1].get("point_order")) or item[0])
		start_s = parse_float(group_rows[0][1].get("segment_start_time") or group_rows[0][1].get("start_time"))
		end_s = parse_float(group_rows[-1][1].get("segment_end_time") or group_rows[-1][1].get("end_time"))
		if start_s is None:
			start_s = _timestamp_ms_to_seconds(group_rows[0][1].get("t_in") or group_rows[0][1].get("timestamp_ms"))
		if end_s is None:
			end_s = _timestamp_ms_to_seconds(group_rows[-1][1].get("t_out") or group_rows[-1][1].get("timestamp_ms"))
		for position, (row_index, row) in enumerate(group_rows):
			direct_s = _timestamp_ms_to_seconds(row.get("timestamp_ms") or row.get("t_in") or row.get("time"))
			if direct_s is not None:
				result[row_index] = direct_s
				continue
			if start_s is None and end_s is None:
				result[row_index] = None
				continue
			if start_s is None or end_s is None or len(group_rows) <= 1:
				result[row_index] = start_s if start_s is not None else end_s
				continue
			ratio = position / max(1, len(group_rows) - 1)
			result[row_index] = start_s + (end_s - start_s) * ratio
	return result


def build_reconstruction_rows(uid: str, rows: list[dict[str, Any]], truth_points: list[Point]) -> list[dict[str, Any]]:
	from adapters.signal_gps_compare.build_batch import nearest_line_distance

	result: list[dict[str, Any]] = []
	row_times = _build_interpolated_row_times(rows)
	for index, row in enumerate(rows):
		point = row_point_wgs(row)
		if point is None:
			continue
		distance, segment_index = nearest_line_distance(point, truth_points)
		state = "gps_match" if distance <= GPS_COVERAGE_THRESHOLD_M else "gps_mismatch"
		point_time_s = row_times.get(index)
		result.append(
			{
				"uid": uid,
				"point_index": index,
				"latitude": f"{point.lat:.8f}",
				"longitude": f"{point.lon:.8f}",
				"timestamp_ms": _seconds_to_timestamp_ms(point_time_s),
				"state": state,
				"status": state,
				"distance_to_gps_m": f"{distance:.2f}",
				"matched_gps_file": "gps_truth.csv",
				"gps_segment_index": segment_index,
				"source_x": "",
				"source_y": "",
			}
		)
	return result


