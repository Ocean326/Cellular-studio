def parse_float(value: Any) -> float | None:
	try:
		return float(str(value).strip())
	except (TypeError, ValueError):
		return None


def read_points_csv(path: Path) -> list[dict[str, Any]]:
	with path.open(encoding="utf-8", newline="") as handle:
		return list(csv.DictReader(handle))


def row_point(row: dict[str, Any]) -> Point | None:
	lat = parse_float(row.get("latitude") or row.get("lat"))
	lon = parse_float(row.get("longitude") or row.get("lon"))
	if lat is None or lon is None:
		return None
	return Point(lat=lat, lon=lon)


def row_point_wgs(row: dict[str, Any]) -> Point | None:
	return row_point(row)


def read_kml_truth_wgs(path: Path) -> Any:
	from adapters.signal_gps_compare.build_batch import Reconstruction

	ns = {
		"k": "http://www.opengis.net/kml/2.2",
		"gx": "http://www.google.com/kml/ext/2.2",
	}
	root = ET.parse(path).getroot()
	points: list[Point] = []
	source_xy: list[tuple[float, float]] = []
	for element in root.findall(".//gx:coord", ns):
		parts = str(element.text or "").split()
		if len(parts) < 2:
			continue
		lon = parse_float(parts[0])
		lat = parse_float(parts[1])
		if lon is None or lat is None:
			continue
		points.append(Point(lat=lat, lon=lon))
		source_xy.append((lon, lat))
	if not points:
		for element in root.findall(".//k:coordinates", ns):
			for token in str(element.text or "").split():
				parts = token.split(",")
				if len(parts) < 2:
					continue
				lon = parse_float(parts[0])
				lat = parse_float(parts[1])
				if lon is None or lat is None:
					continue
				points.append(Point(lat=lat, lon=lon))
				source_xy.append((lon, lat))
	deduped_points: list[Point] = []
	deduped_source_xy: list[tuple[float, float]] = []
	for point, source in zip(points, source_xy):
		if deduped_points and abs(deduped_points[-1].lat - point.lat) < 1.0e-10 and abs(deduped_points[-1].lon - point.lon) < 1.0e-10:
			continue
		deduped_points.append(point)
		deduped_source_xy.append(source)
	if len(deduped_points) < 2:
		raise ValueError(f"gps truth kml has fewer than 2 usable points: {path}")
	return Reconstruction(path=path, points=deduped_points, source_xy=deduped_source_xy)


