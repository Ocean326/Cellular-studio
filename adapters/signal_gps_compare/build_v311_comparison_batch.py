def _parse_seconds_value(value: Any) -> float | None:
	if value is None or value == "":
		return None
	try:
		number = float(str(value).strip())
	except (TypeError, ValueError):
		return None
	if number > 10_000_000_000:
		number /= 1000.0
	return number


def _row_time_seconds(row: dict[str, Any], *keys: str) -> float | None:
	for key in keys:
		value = row.get(key)
		parsed = _parse_seconds_value(value)
		if parsed is not None:
			return parsed
	return None


def _row_signal_point(row: dict[str, Any]) -> Point | None:
	point = row_point_wgs(row)
	if point is None:
		return None
	return point


def _centroid(points: list[Point]) -> Point:
	return Point(
		lat=sum(point.lat for point in points) / len(points),
		lon=sum(point.lon for point in points) / len(points),
	)


def _build_endpoint_stay_row(uid: str, side: str, signal_rows: list[dict[str, Any]], anchor: Point, core_time_s: float) -> dict[str, Any] | None:
	if not signal_rows:
		return None
	sorted_rows = sorted(
		signal_rows,
		key=lambda row: abs((_row_time_seconds(row, "t_in", "timestamp_ms", "time") or core_time_s) - core_time_s),
	)
	near_rows: list[dict[str, Any]] = []
	for row in sorted_rows:
		point = _row_signal_point(row)
		if point is None:
			continue
		if distance_m(point, anchor) <= STAY_ENDPOINT_SEARCH_RADIUS_M:
			near_rows.append(row)
	if not near_rows:
		near_rows = sorted_rows[: min(3, len(sorted_rows))]
	points = [point for row in near_rows if (point := _row_signal_point(row)) is not None]
	if not points:
		return None
	if len(points) <= 2:
		points = [*points, anchor]
	center = _centroid(points)
	distances = [distance_m(center, point) for point in points]
	dispersion_m = max(distances) if distances else 0.0
	cids = {str(row.get("cid") or row.get("CID") or "").strip() for row in near_rows if str(row.get("cid") or row.get("CID") or "").strip()}
	signal_count = len(near_rows)
	unique_cid_count = len(cids)
	base_radius = 160.0 / max(1.0, signal_count ** 0.5) + 120.0 / max(1.0, max(1, unique_cid_count) ** 0.5)
	if signal_count < 3:
		base_radius += 80.0
	if unique_cid_count < 2:
		base_radius += 40.0
	radius_m = max(STAY_RADIUS_MIN_M, min(STAY_RADIUS_MAX_M, max(base_radius, dispersion_m * 1.25 + 35.0)))
	times = [
		t
		for row in near_rows
		for t in (
			_row_time_seconds(row, "t_in", "timestamp_ms", "time"),
			_row_time_seconds(row, "t_out", "timestamp_ms", "time"),
		)
		if t is not None
	]
	if not times:
		return None
	if side == "start":
		start_time = min(times)
		end_time = core_time_s
		od_type = "start_stay"
	else:
		start_time = core_time_s
		end_time = max(times)
		od_type = "end_stay"
	if end_time <= start_time:
		end_time = start_time + 1.0
	return {
		"start_latitude": f"{center.lat:.10f}",
		"start_longitude": f"{center.lon:.10f}",
		"end_latitude": f"{center.lat:.10f}",
		"end_longitude": f"{center.lon:.10f}",
		"start_time": f"{start_time:.1f}",
		"end_time": f"{end_time:.1f}",
		"time_diff": f"{end_time - start_time:.1f}",
		"speed": "0",
		"is_stationary": "True",
		"od_type": od_type,
		"stay_radius_m": f"{radius_m:.1f}",
		"stay_signal_count": signal_count,
		"stay_unique_cid_count": unique_cid_count,
		"stay_radius_formula": "max(spatial_dispersion*1.25+35,160/sqrt(signal_count)+120/sqrt(unique_cid_count)+sparse_penalty)",
		"stay_window_pad_s": STAY_WINDOW_PAD_S,
	}


def build_od_rows_with_stays(uid: str, source_rows: list[dict[str, Any]], signal_rows: list[dict[str, Any]], truth_points: list[Point], core_start_ms: int | None, core_end_ms: int | None) -> list[dict[str, Any]]:
	if core_start_ms is None or core_end_ms is None or not truth_points:
		rows = [dict(row) for row in source_rows]
	else:
		core_start_s = core_start_ms / 1000.0
		core_end_s = core_end_ms / 1000.0
		pre_rows = [
			row for row in signal_rows
			if (time_s := _row_time_seconds(row, "t_in", "timestamp_ms", "time")) is not None
			and core_start_s - STAY_WINDOW_PAD_S <= time_s < core_start_s
		]
		post_rows = [
			row for row in signal_rows
			if (time_s := _row_time_seconds(row, "t_in", "timestamp_ms", "time")) is not None
			and core_end_s < time_s <= core_end_s + STAY_WINDOW_PAD_S
		]
		rows = []
		start_stay = _build_endpoint_stay_row(uid, "start", pre_rows, truth_points[0], core_start_s)
		end_stay = _build_endpoint_stay_row(uid, "end", post_rows, truth_points[-1], core_end_s)
		if start_stay:
			rows.append(start_stay)
		for row in source_rows:
			next_row = dict(row)
			next_row.setdefault("od_type", "move")
			next_row.setdefault("stay_radius_m", "")
			next_row.setdefault("stay_signal_count", "")
			next_row.setdefault("stay_unique_cid_count", "")
			next_row.setdefault("stay_radius_formula", "")
			next_row.setdefault("stay_window_pad_s", "")
			rows.append(next_row)
		if end_stay:
			rows.append(end_stay)
	rows.sort(key=lambda row: _row_time_seconds(row, "start_time", "t_in", "timestamp_ms") or 0.0)
	return rows


def coverage_stats_from_rows(rows: list[dict[str, Any]], threshold_m: float) -> tuple[float, float]:
	covered = 0.0
	total = 0.0
	for row in rows:
		try:
			segment_length_m = float(row.get("segment_length_m") or 0.0)
		except (TypeError, ValueError):
			segment_length_m = 0.0
		if segment_length_m <= 0.0:
			continue
		total += segment_length_m
		try:
			distance_m = float(row.get("matched_distance_m") or "inf")
		except (TypeError, ValueError):
			distance_m = float("inf")
		if distance_m <= threshold_m:
			covered += segment_length_m
	return covered, total


def is_route04_uid(uid: str) -> bool:
	return "测试4" in uid or "route_04" in uid


def build_gps_coverage_scenarios(
	coverage_rows_by_uid: dict[str, list[dict[str, Any]]],
) -> tuple[list[dict[str, Any]], dict[str, Any]]:
	scenarios: list[dict[str, Any]] = []
	for threshold_m in GPS_COVERAGE_SCENARIO_THRESHOLDS_M:
		by_uid: dict[str, Any] = {}
		total_covered = 0.0
		total_length = 0.0
		non04_covered = 0.0
		non04_length = 0.0
		non04_percents: list[float] = []
		for uid, rows in coverage_rows_by_uid.items():
			covered, length = coverage_stats_from_rows(rows, float(threshold_m))
			percent = covered / length * 100.0 if length else 0.0
			by_uid[uid] = {
				"accuracy_percent": round(percent, 2),
				"covered_length_m": round(covered, 2),
				"total_length_m": round(length, 2),
			}
			total_covered += covered
			total_length += length
			if not is_route04_uid(uid):
				non04_covered += covered
				non04_length += length
				non04_percents.append(percent)
		scenarios.append(
			{
				"threshold_meters": threshold_m,
				"overall_accuracy_percent": round(total_covered / total_length * 100.0, 2) if total_length else 0.0,
				"excluding_route04_accuracy_percent": round(non04_covered / non04_length * 100.0, 2) if non04_length else 0.0,
				"excluding_route04_min_uid_accuracy_percent": round(min(non04_percents), 2) if non04_percents else 0.0,
				"by_uid": by_uid,
			}
		)

	def first_threshold(predicate) -> float | None:
		for scenario in scenarios:
			if predicate(scenario):
				return float(scenario["threshold_meters"])
		return None

	recommendation = {
		"strict_50m_overall_accuracy_percent": scenarios[0]["overall_accuracy_percent"] if scenarios else 0.0,
		"strict_50m_excluding_route04_accuracy_percent": scenarios[0]["excluding_route04_accuracy_percent"] if scenarios else 0.0,
		"overall_ge_90_threshold_meters": first_threshold(lambda item: item["overall_accuracy_percent"] >= 90.0),
		"excluding_route04_ge_90_threshold_meters": first_threshold(lambda item: item["excluding_route04_accuracy_percent"] >= 90.0),
		"all_non04_each_ge_90_threshold_meters": first_threshold(lambda item: item["excluding_route04_min_uid_accuracy_percent"] >= 90.0),
	}
	return scenarios, recommendation


