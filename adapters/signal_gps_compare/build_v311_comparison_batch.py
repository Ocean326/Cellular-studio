def sample_evenly_indices(count: int, target_count: int) -> list[int]:
	if count <= 0:
		return []
	if count <= target_count:
		return list(range(count))
	if target_count <= 1:
		return [count // 2]
	last_index = count - 1
	result: list[int] = []
	for index in range(target_count):
		candidate = round(index * last_index / (target_count - 1))
		if not result or result[-1] != candidate:
			result.append(candidate)
	return result


def sample_irregular_gate_indices(count: int, route_index: int) -> list[int]:
	if count <= 0:
		return []
	pattern = GATE_FRACTION_PATTERNS[(route_index - 1) % len(GATE_FRACTION_PATTERNS)]
	target_count = GATE_TARGET_COUNTS[(route_index - 1) % len(GATE_TARGET_COUNTS)]
	last_index = max(0, count - 1)
	indices: list[int] = []
	for item_index, fraction in enumerate(pattern[:target_count]):
		jitter = (((route_index + item_index * 2) % 5) - 2) * 0.012
		index = round(max(0.0, min(1.0, fraction + jitter)) * last_index)
		if indices and index <= indices[-1]:
			index = min(last_index, indices[-1] + max(1, count // 18))
		indices.append(index)
	return sorted(dict.fromkeys(index for index in indices if 0 <= index <= last_index))


