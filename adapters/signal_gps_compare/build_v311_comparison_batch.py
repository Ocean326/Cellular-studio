def _parse_timestamp_ms(value: Any) -> int | None:
	try:
		number = float(str(value).strip())
	except (TypeError, ValueError):
		return None
	if number <= 0:
		return None
	if number < 10_000_000_000:
		number *= 1000.0
	return int(round(number))


def _timestamp_ms_to_seconds(value: Any) -> float | None:
	parsed = _parse_timestamp_ms(value)
	if parsed is None:
		return None
	return parsed / 1000.0


def _seconds_to_timestamp_ms(value: float | None) -> str:
	if value is None:
		return ""
	return str(int(round(value * 1000.0)))


