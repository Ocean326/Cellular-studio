def seeded_rng_for_text(text: str) -> random.Random:
	digest = hashlib.sha256(text.encode("utf-8")).hexdigest()
	return random.Random(int(digest[:16], 16))


def build_lbs_rows(
	uid: str,
	truth_points: list[Point],
	road_rows: list[dict[str, Any]],
	start_ms: int | None,
	end_ms: int | None,
	source_file: str,
) -> list[dict[str, Any]]:
	from adapters.signal_gps_compare.build_batch import interpolate_time_ms

	if len(truth_points) < 2 or not road_rows:
		return []
	matched_indices: list[int] = []
	for row in road_rows:
		if str(row.get("matched") or "").strip() != "1":
			continue
		try:
			idx = int(float(str(row.get("truth_segment_index") or "")))
		except ValueError:
			continue
		if 0 <= idx < len(truth_points) - 1:
			matched_indices.append(idx)
	if not matched_indices:
		return []

	total_route_length_m = sum(distance_m(truth_points[i], truth_points[i + 1]) for i in range(len(truth_points) - 1))
	if total_route_length_m <= 0.0:
		return []
	target_segment_count = 1 if total_route_length_m <= LBS_SHORT_ROUTE_MAX_M else 2
	per_segment_cap_m = (total_route_length_m * LBS_COVERAGE_CAP_FRACTION) / target_segment_count
	if per_segment_cap_m < 120.0:
		return []
	rng = seeded_rng_for_text(f"lbs:{uid}")

	runs: list[tuple[int, int, float]] = []
	run_start = matched_indices[0]
	prev = matched_indices[0]
	for idx in matched_indices[1:]:
		if idx == prev + 1:
			prev = idx
			continue
		length = sum(distance_m(truth_points[i], truth_points[i + 1]) for i in range(run_start, prev + 1))
		runs.append((run_start, prev, length))
		run_start = idx
		prev = idx
	length = sum(distance_m(truth_points[i], truth_points[i + 1]) for i in range(run_start, prev + 1))
	runs.append((run_start, prev, length))
	runs = [run for run in runs if run[2] >= min(LBS_MIN_SEGMENT_M, per_segment_cap_m)]
	if not runs:
		return []

	route_len = len(truth_points)
	if target_segment_count <= 1:
		anchors = (rng.uniform(0.22, 0.78),)
	else:
		first = rng.uniform(0.14, 0.42)
		second = rng.uniform(0.58, 0.88)
		if second - first < 0.30:
			second = min(0.9, first + 0.30)
		anchors = (first, second)
	selected_ranges: list[tuple[int, int]] = []
	selected_indices: list[list[int]] = []
	selected_target_lengths: list[float] = []
	for anchor_index, anchor in enumerate(anchors):
		target_segment_m = min(
			rng.uniform(*LBS_TARGET_SEGMENT_RANGE_M),
			LBS_MAX_SEGMENT_M,
			per_segment_cap_m,
		)
		if target_segment_m < 120.0:
			continue
		min_segment_m = min(LBS_MIN_SEGMENT_M, target_segment_m)
		target = anchor * route_len
		candidates = sorted(runs, key=lambda run: abs(max(run[0], min(run[1], int(round(target)))) - target))
		for run_start, run_end, _run_length in candidates:
			center = max(run_start, min(run_end, int(round(target))))
			left = center
			right = center
			current_length = 0.0
			while current_length < target_segment_m and (left > run_start or right < run_end):
				left_length = distance_m(truth_points[left - 1], truth_points[left]) if left > run_start else float("inf")
				right_length = distance_m(truth_points[right], truth_points[right + 1]) if right < run_end else float("inf")
				if current_length + min(left_length, right_length) > target_segment_m and current_length >= min_segment_m:
					break
				if right_length <= left_length and right < run_end:
					current_length += right_length
					right += 1
				elif left > run_start:
					current_length += left_length
					left -= 1
				else:
					break
			if current_length < min_segment_m:
				continue
			if any(not (right < item_start - 8 or left > item_end + 8) for item_start, item_end in selected_ranges):
				continue
			selected_ranges.append((left, right))
			selected_indices.append(list(range(left, right + 1)))
			selected_target_lengths.append(target_segment_m)
			break

	rows: list[dict[str, Any]] = []
	for segment_index, segment_indices in enumerate(selected_indices, start=1):
		if len(segment_indices) < 2:
			continue
		segment_length = sum(
			distance_m(truth_points[i], truth_points[i + 1])
			for i in range(segment_indices[0], segment_indices[-1])
		)
		segment_id = f"LBS-{segment_index:02d}"
		for point_order, point_index in enumerate(segment_indices):
			point = truth_points[point_index]
			rows.append(
				{
					"uid": uid,
					"segment_id": segment_id,
					"point_index": point_order,
					"latitude": f"{point.lat:.8f}",
					"longitude": f"{point.lon:.8f}",
					"timestamp_ms": interpolate_time_ms(start_ms, end_ms, point_index, len(truth_points), 1000),
					"source_gps_index": point_index,
					"source_file": source_file,
					"segment_length_m": f"{segment_length:.2f}",
					"status": "lbs_assist",
				}
			)
	return rows


